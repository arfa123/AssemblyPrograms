.DATA
	MSG1 DB 'Enter 1st no.:$'
	MSG2 DB 'Enter 2nd no.:$'
	EXTRN X:BYTE
	EXTRN Y:BYTE
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	PUBLIC INP FAR
MAIN ENDP
INP PROC FAR
	MOV DX,OFFSET MSG1
	MOV AH,9
	INT 21H
	MOV AH,1
	INT 21H
	MOV X,AL
	MOV DX,OFFSET MSG2
	MOV AH,9
	INT 21H
	MOV AH,1
	INT 21H
	MOV Y,AL
	RET
INP ENDP
END