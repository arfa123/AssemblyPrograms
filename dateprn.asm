.MODEL SMALL
.STACK 32H
.DATA
	MTH DW D0,D1,D2,D3,D4,D5,D6,D7,D8,D9,D10,D11,D12
	D0 DB '$'
	D1 DB ' JAN,$'
	D2 DB ' FEB,$'
	D3 DB ' MAR,$'
	D4 DB ' APR,$'
	D5 DB ' MAY,$'
	D6 DB ' JUN,$'
	D7 DB ' JUL,$'
	D8 DB ' AUG,$'
	D9 DB ' SEP,$'
	D10 DB ' OCT,$'
	D11 DB ' NOV,$'
	D12 DB ' DEC,$'
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV AH,2AH
	INT 21H
	CALL DATEPRN
	MOV BX,OFFSET MTH
	MOV AH,2AH
	INT 21H
	CALL MTHPRN
	CALL YEARP
	MOV AH,4CH
	INT 21H
MAIN ENDP
DATEPRN PROC
	POP BP
	MOV AL,DL
	MOV DL,10
	MOV BH,0
	MOV CX,0
	DIV1:
		MOV AH,0
		DIV DL
		MOV BL,AH
		PUSH BX
		INC CX
		CMP AL,0
		JZ PRN
	JMP DIV1
	PRN:
		POP DX
		ADD DL,30H
		MOV AH,2
		INT 21H
	LOOP PRN
	PUSH BP
	RET
DATEPRN ENDP
MTHPRN PROC
	MOV AL,DH
	MOV AH,0
	MOV DI,AX
	ADD DI,DI
	MOV DX,[BX+DI]
	MOV AH,9
	INT 21H
	RET
MTHPRN ENDP
YEARP PROC
	POP BP
	MOV AX,CX
	MOV DL,10
	MOV BH,0
	MOV CX,0
	DIV2:
		DIV DL
		MOV BL,AH
		PUSH BX
		MOV AH,0
		INC CX
		CMP AL,0
		JZ PRN1
	JMP DIV2
	PRN1:
		POP DX
		ADD DL,30H
		MOV AH,2
		INT 21H
	LOOP PRN1
	PUSH BP
	RET
YEARP ENDP
END