.MODEL SMALL
.DATA
	TABLE DB '0123456789ABCDEF'
	NM DB 15
.CODE
	MOV AX,@DATA
	MOV DS,AX
	MOV AX,NM
	MOV DL,16
	MOV BX,0
	MOV CX,0
	DIV1:
		MOV AH,0
		DIV DL
		MOV BL,AH
		PUSH BX
		INC CX
		CMP AL,0
		JZ EXT
		
	JMP DIV1
	EXT:
	MOV BX,OFFSET TABLE
	MOV AH,2
	MOV AL,0
	PRN:
		POP DX
		MOV AL,DL
		XLAT
		MOV DL,AL
		INT 21H
	LOOP PRN
	MOV AH,4CH
	INT 21H
END
		