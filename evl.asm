.MODEL SMALL
.DATA
	X DB 4 DUP(0)
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	CALL INP
	CALL EVL
	MOV AH,4CH
	INT 21H
MAIN ENDP
INP PROC
	POP BP
	MOV BH,0
	MOV CX,0
	MOV AH,1
	L:
		INT 21H
		CMP AL,0DH
		JZ EXT
		SUB AL,30H
		MOV BL,AL
		PUSH BX
		INC CX
	JMP L
	EXT:
	PUSH BP
	RET
INP ENDP
EVL PROC
	POP BP
	MOV DL,1
	MOV BX,0
	L1:
		POP AX
		MUL DL
		ADD BX,AX
		MOV AL,DL
		MOV DL,10
		MUL DL
		MOV DL,AL
	LOOP L1
	MOV SI,OFFSET X
	MOV [SI],BX
	PUSH BP
	RET
EVL ENDP
END