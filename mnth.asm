.MODEL SMALL
.STACK 32H
.DATA
	MTH DW D0,M0,M1,M2,M3,M4,M5,M6,M7,M8,M9,MA,MB
	D0 DB '$'
	M0 DB 'JAN$'
	M1 DB 'FEB$'
	M2 DB 'MAR$'
	M3 DB 'APR$'
	M4 DB 'MAY$'
	M5 DB 'JUN$'
	M6 DB 'JUL$'
	M7 DB 'AUG$'
	M8 DB 'SEP$'
	M9 DB 'OCT$'
	MA DB 'NOV$'
	MB DB 'DEC$'
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV BX,OFFSET MTH
	MOV AH,2AH
	INT 21H
	CALL MTHPRN
	MOV AH,4CH
	INT 21H
MAIN ENDP
MTHPRN PROC
	MOV AL,DH
	MOV AH,0
	MOV DI,AX
	ADD DI,DI
	MOV DX,[BX+DI]
	MOV AH,9
	INT 21H
	RET
MTHPRN ENDP
END