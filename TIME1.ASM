.MODEL SMALL
.STACK 32H
.CODE
MAIN PROC
	CALL HOUR
	CALL MIN
	CALL SEC
	MOV AH,4CH
	INT 21H
MAIN ENDP
HOUR PROC
	MOV AH,2CH
	INT 21H
	MOV DL,10
	MOV BH,0
	MOV AL,CH
	MOV CX,0
	L1:
		MOV AH,0
		DIV DL
		MOV BL,AH
		PUSH BX
		INC CX
		CMP AL,0
		JZ EXT
	JMP L1
	EXT:
	MOV AH,2
	PRN1:
		POP DX
		ADD DL,30H
		INT 21H
	LOOP PRN1
	MOV DL,':'
	MOV AH,2
	INT 21H
	RET
HOUR ENDP
MIN PROC
	MOV AH,2CH
	INT 21H
	MOV DL,10
	MOV BH,0
	MOV AL,CL
	MOV CX,0
	L2:
		MOV AH,0
		DIV DL
		MOV BL,AH
		PUSH BX
		INC CX
		CMP AL,0
		JZ EXT1
	JMP L2
	EXT1:
	MOV AH,2
	PRN2:
		POP DX
		ADD DL,30H
		INT 21H
	LOOP PRN2
	MOV DL,':'
	MOV AH,2
	INT 21H
	RET
MIN ENDP
SEC PROC
	MOV AH,2CH
	INT 21H
	MOV AL,DH
	MOV DL,10
	MOV BH,0
	MOV CX,0
	L3:
		MOV AH,0
		DIV DL
		MOV BL,AH
		PUSH BX
		INC CX
		CMP AL,0
		JZ EXT2
	JP L3
	EXT2:
	MOV AH,2
	PRN3:
		POP DX
		ADD DL,30H
		INT 21H
	LOOP PRN3
	RET
SEC ENDP
END