.MODEL SMALL
.STACK 32H
.DATA
	PTR1 DW D0,D1,D2,D3,D4,D5,D6
	D0 DB 'SUNDAY$'
	D1 DB 'MONDAY$'
	D2 DB 'TUESDAY$'
	D3 DB 'WEDNESDAY$'
	D4 DB 'THURSDAY$'
	D5 DB 'FRIDAY$'
	D6 DB 'SATURDAY$'
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV BX,OFFSET PTR1
	MOV AH,2AH
	INT 21H
	CALL DAYPRN
	MOV AH,4CH
	INT 21H
MAIN ENDP
DAYPRN PROC
	MOV AH,0
	MOV DI,AX
	ADD DI,DI
	MOV DX,[BX+DI]
	MOV AH,9
	INT 21H
	RET
DAYPRN ENDP
END