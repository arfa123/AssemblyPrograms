.MODEL SMALL
.STACK 32H
.DATA
	TABLE DB 100 DUP(0)
	ST1 DW 1 DUP(0)
	ST2 DW 1 DUP(0)
	ST3 DW 1 DUP(0)
	NLF DB 10,13,'$'
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV SI,OFFSET TABLE
	MOV CX,9
	MOV BL,2
	L1:
		MOV DL,1
		PUSH CX
		MOV CX,10
		L2:
			MOV AL,BL	
			MUL DL
			MOV [SI],AL
			INC SI
			INC DL
		LOOP L2
		INC BL
		POP CX
	LOOP L1
	MOV SI,OFFSET TABLE
	MOV DI,2
	MOV CX,9
	L3:
		MOV BP,1
		MOV ST1,CX
		MOV CX,10
		L4:
			MOV ST2,CX
			CALL STR
			MOV DL,10
			MOV BH,0
			MOV CX,0
			MOV AL,[SI]
			DIV3:
				MOV AH,0
				DIV DL
				MOV BL,AH
				PUSH BX
				INC CX
				CMP AL,0
				JZ L7
			JMP DIV3
			L7:
				MOV AH,2
				POP DX
				ADD DL,30H
				INT 21H
			LOOP L7
			MOV CX,ST2
			INC BP
			INC SI
			MOV DX,OFFSET NLF
			MOV AH,9
			INT 21H
		LOOP L4
		MOV CX,ST1
		INC DI
	LOOP L3	
	MOV AH,4CH
	INT 21H
MAIN ENDP
STR PROC
	POP ST3
	MOV DL,10
	MOV BH,0
	MOV CX,0
	MOV AX,DI
	DIV1:
		MOV AH,0
		DIV DL
		MOV BL,AH
		PUSH BX
		INC CX
		CMP AL,0
		JZ L5
	JMP DIV1
	L5:
		MOV AH,2
		POP DX
		ADD DL,30H
		INT 21H
	LOOP L5
	MOV DL,'x'
	INT 21H
	MOV AX,BP
	MOV DL,10
	MOV BH,0
	MOV CX,0
	DIV2:
		MOV AH,0
		DIV DL
		MOV BL,AH
		PUSH BX
		INC CX
		CMP AL,0
		JZ L6
	JMP DIV2
	L6:
		MOV AH,2
		POP DX
		ADD DL,30H
		INT 21H
	LOOP L6
	MOV DL,'='
	INT 21H
	PUSH ST3
RET
STR ENDP
END
			